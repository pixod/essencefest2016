<h2><span id="hd">DOUG E. FRESH</span>
<BR>
Dubbed the &quot;World's Greatest Entertainer&quot; for his unrivaled ability torock a crowd, Harlem native&nbsp;Doug&nbsp;E.&nbsp;Fresh&nbsp;began his musical career atage 13. The originator of the human beat box (vocally simulating the sound of drums and other musical instruments), he spawned an international hip-hop trend. Best known for the two-sided, multi-platinum hits &quot;The Show&quot; and &quot;La Di Da Di,&quot; his groundbreaking successes and firsts, like being the first rapper to play Africa and the Caribbean, heralded the global popularity of hip-hop.<br>
<br>
As concerned with the welfare of others as he is with rockin' the mic,<br>
Fresh&nbsp;has embraced hip-hop activism and used his voice to speak out against a variety of social ills. Along the way, he has nurtured<br>
rising talent, including the likes of MC Ricky D (AKA Slick Rick), P.<br>
Diddy, Biz Markie and numerous newcomers during his stint as host (and unofficial mentor) of It's Showtime at the Apollo.<br>
<br>
Throughout his 20-year career,&nbsp;Fresh&nbsp;has collaborated with the world's top artists. He's performed or recorded with fellow rap legends,including Tupac Shakur, Biggie Smalls, Eminem, P. Diddy and Dr. Dre. He's also worked with artists in reggae (Beenie Man, Sly &amp; Robbie and Poppa San), R&amp;B (Prince, Roberta Flack, Chaka Kahn, and Stevie Wonder), jazz (George Benson, Grover Washington and Bobbie McFerrin) and gospel (Rev. Robert Lowe and Generations). Widely in demand around the globe,&nbsp;Fresh&nbsp;has recorded with Rahzel and Japan's DJ Hasebe, a Swedish hip-hop group called the South Street Rockers, Italy's Claudio<br>
Risi and French rappers Supa Saian Crew.</h2>
